# Activity: Simple Decorator
#
# Goal:
# Understand how a decorator can run code before and after a function.
#
# Instructions:
# 1. Create a decorator function named `decorator`.
#    - It should take a function as an argument.
#    - It should print "Before calling the function."
#    - It should call the original function.
#    - It should print "After calling the function."
#
# 2. Create a function named `greet` that prints "Hello, World!".
#
# 3. Apply the decorator to `greet` using the @decorator syntax.
#
# 4. Call `greet()` and observe the output.
#
# Expected Output:
# Before calling the function.
# Hello, World!
# After calling the function.

